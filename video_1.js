// What is Ecmascript  ?


/*
ECMAScript is like the rulebook for JavaScript. 
It sets the basic rules for how JavaScript should work, including how the language is written, how it handles data 
(like numbers and strings), and what built-in features it has (like arrays and functions).
Think of ECMAScript as the blueprint, and JavaScript as the actual building. 
Web browsers follow the ECMAScript rules to make sure JavaScript works the same way everywhere.
Each version of ECMAScript (like ES5, ES6, etc.) introduces new tools and features that make JavaScript more powerful
and easier to use. For example, ECMAScript 6 (ES6) added cool features like arrow functions
 and promises that make writing code simpler.
 */
